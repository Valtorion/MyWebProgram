<h4>3.5.5 Защита нагрузки по мощности потребления (по умолчанию отключена)</h4>
<p>Во время работы ЕМ-129 постоянно измеряет величину мощности, потребляемой нагрузкой.</p>
<p>При  повышении  мощности  нагрузки  выше  порога  14  490  Вт  («Порог  отключения»),  нагрузка 
отключится через время 5,0 с («Задержка отключения»). </p>
<p>После отключения нагрузки произойдет возврат к нормальному режиму работы через время АПВ. 
Работа изделия в режиме аварии описана в пункте 3.5.6 («Отключение нагрузки из-за аварии»). </p>

<h2>Отключение нагрузки из-за аварии</h2>
<p>При возникновении аварийной ситуации (превышение напряжения, превышение тока, превышение 
мощности  и  т.д.),  нагрузка  отключается,  начинается  счет  времени  АПВ  и  индикатор  состояния 
(рисунок 1 поз.2) начинает светиться красным цветом. </p>
<p>При  пропадании  аварийной  ситуации  индикатор  состояния  начинает  мигать  красным  цветом, 
указывая, что выполняется счет времени АПВ, по окончании которого нагрузка будет автоматически 
включена. </p>
<p>Если  счет  времени  АПВ  закончился  до  пропадания  аварийной  ситуации,  то  нагрузка  будет 
включена без задержки после пропадания аварийной ситуации. </p>
<p>Если было превышено число попыток автоматических повторных включений (для защиты по току и 
по  мощности  «3»,  для  защиты  по  напряжению    «нет»),  изделие  заблокирует  включение  нагрузки,  а 
индикатор состояния будет постоянно светиться красным цветом. Для восстановления работы ЕМ-129 
необходимо отключить его от сети, подождать 5 с и снова включить. </p>

<h2>Сброс на заводские установки </h2>
<p>На лицевой панели изделия нажать и удерживать кнопку «Wi-Fi» в течение 20 с.</p>
<p>По  истечении  времени  20  с  индикатор  состояния  начнет  светиться  лиловым  цветом,  отпустить 
кнопку «Wi-Fi». </p>
<p>По  завершении  сброса  на  заводские  установки  индикатор  состояния  перестанет  светиться 
лиловым цветом, а изделие выполнит автоматическую перезагрузку. </p>
<p>Изделие сброшено на заводские установки и готово к использованию.</p>

