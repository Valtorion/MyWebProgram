<h1>Защита нагрузки</h1>
<h2>Защита нагрузки по напряжению сети</h2>
<p>Во время работы ЕМ-129 постоянно измеряет величину сетевого напряжения.</p>
<p>При повышении напряжения выше порога 255В («Верхний порог отключения»), нагрузка отключится 
через время 0,5 с («Задержка отключения по верхнему порогу»). </p>
<p>При дальнейшем повышении напряжения еще на 30В  (285 В), отключение нагрузки произойдет с 
фиксированной выдержкой времени 0,2 с. </p>
<p>При повышении напряжения свыше 300 ± 10В, отключение нагрузки произойдет с фиксированной 
выдержкой времени 0,05 с. </p>
<p>После  отключения  нагрузки,  если  напряжение  сети  снизилось  ниже  250  В  («Верхний  порог 
отключения» 255 В минус «Гистерезис» 5 В), произойдет возврат к автоматическому режиму работы 
через время АПВ (5 с). </p>

<p>При снижении  напряжения ниже  порога  190В  («Нижний  порог  отключения»),  нагрузка отключится 
через время 12,0 с («Задержка отключения по нижнему порогу»). </p>

<p>При  дальнейшем  снижении  напряжения  ниже  145В,  отключение  нагрузки  произойдет  с 
фиксированной выдержкой времени 0,25 с. </p>
<p>После  отключения  нагрузки,  если  напряжение  сети  повысится  выше  195В  («Нижний  порог 
отключения» 190В плюс «Гистерезис» 5 В), произойдет возврат к автоматическому режиму работы 
через время АПВ.</p>
<p>Работа изделия в режиме аварии описана в пункте 3.5.6 («Отключение нагрузки из-за аварии»). </p>

<h2>Защита нагрузки по частоте (по умолчанию отключена)</h2>
<p>Во время работы ЕМ-129 постоянно измеряет величину частоты сетевого напряжения.</p>
<p>При повышении частоты выше порога 51,0 Гц («Верхний порог отключения»), нагрузка отключится 
через время 10,0 с («Задержка отключения»). </p>
<p>После отключения нагрузки, если частота сети снизилась ниже 51,0 Гц («Верхний порог отключе-
ния»), произойдет возврат к автоматическому режиму работы через время АПВ. 
При  снижении  частоты  ниже  порога  49,0  Гц  («Нижний  порог  отключения»),  нагрузка  отключится 
через время 10,0 с («Задержка отключения»). </p>
<p>После отключения нагрузки, если частота сети повысится выше 49,0 Гц («Нижний порог отключе-
ния»), произойдет возврат к автоматическому режиму работы через время АПВ. </p>
<p>Работа изделия в режиме аварии описана в пункте 3.5.6 («Отключение нагрузки из-за аварии»). </p>

