<h2>Управление кнопкой «Wi-Fi»</h2>
<p>Кнопка «Wi-Fi»(рисунок 1 поз.3)используется для управленияизделием с лицевой панели.При удержании кнопки«Wi-Fi»цвет индикатора состояния(рисунок 1 поз.2)будет менять свой цвет, в зависимостиот длительности удержания кнопки –тем самым индицируя выполняемое действие.Для подтверждения выполнения действия необходимо во время свечения индикатора состояния отпустить кнопку «Wi-Fi».</p>

