<h2>Настройка Wi-Fiподключения</h2>

<figure>
    
        <img src="./Sorce/7mini.jpg" class="minimized" alt="альтернативный текст" onclick="UpImg(this);"/>

    <figcaption>Рисунок 3–Точка доступа EM-129</figcaption>
