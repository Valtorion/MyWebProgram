<p>На  электронном  устройстве  запустить Web-браузер (Chrome, Opera, FireFox, прочее).</p>
<p>В  адресной  строке  браузера  ввести  «http://em.com»  или «http://192.168.4.1»  и  выполнить  переход  по  введенной ссылке.</p>
<p>На   экране   электронного   устройства   откроется Web-интерфейс EM-129 (см. рисунок 4).</p>
<p>Выполнить  настройку Wi-Fiподключения,выбрав  свою сеть и введя пароль:</p>
<ul>
    <li>Имя сети (SSID)–название вашейWi-Fiсети;</li>
    <li>Пароль сети–пароль вашейWi-Fiсети;</li>
    <li>Настройки TCP/IP–настройки IPадреса,  маскиподсети   и адресашлюза(по   умолчанию настраивается автоматически);</li>
    <li>MACадрес–уникальный адрес устройства;</li>
    <li>Пароль  устройства–используется  при  удаленном подключениик устройству (по умолчанию «admin»).</li>
</ul>

<p>Для сохранения настроек нажать кнопку «Далее».</p>
