<p>Для  входа  в  режим  настройки  необходимо  на  лицевой панели  ЕМ-129нажать  и  удерживать  кнопку  «Wi-Fi» (рисунок 1 поз.3) в течение 5-6 секунд.</p>
<p>При  этом  индикатор состояния  (рисунок  1  поз.2)будетмигать сине-красным цветом и ЕМ-129создаст точку доступа с  именем  «ЕМ-129_хххххх»,  где  хххххх–уникальный  код изделия (см. рисунок 3).Отпустить кнопку.</p>
<p>С помощью электронного устройства (ПК с Wi-Fi, телефон, планшет,  ноутбук,  прочее)  выполнить  подключение  к  точке доступа, используя следующие параметры:</p>
<ul>
    <li>Имя точки доступа «ЕМ-129_хххххх»;</li>
    <li>Безопасность «Нет».</li>
</ul>

<p>Примечание-для принудительного выхода из режима настройки Wi-Fiподключения –нажать и удерживать на лицевой панели ЕМ-129кнопку«Wi-Fi» в течение 5-6 секунд. Отпустить  кнопку,при  этом индикатор  состояния перестанет  мигать  сине-красным  цветом,  а  изделие перейдет в режим автоматическойработы.</p>

